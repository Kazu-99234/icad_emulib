//**************************************************************************************
//* 関数名　　:ireq_C
//* 概要  　　:数値の誤差範囲の比較
//* 詳細  　　:
//* 引数　　　:[I] 数値１
//* 　　　　　:[I] 数値２
//* 　　　　　:[I] 誤差
//* 戻り値　　:'=0：一致しない、=1：一致
//* 作成者　　:2022/10/08 FXS 葛
//* 更新者　　:20xx/xx/xx
//* 更新内容　:
